#include "main.h"
/**
 * *_memset - A function that fills memory with a constant byte
 * @s: the pointer
 * @b: the constant
 * @n: the max bytes
 * Return: the pointer s
 */
char *_memset(char *s, char b, unsigned int n);
{
	unsigned int i;

	for (i = 0; i > 0; i++, n--)
	{
		s[i] = b;
	}
	return (s);
}
